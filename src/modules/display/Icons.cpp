const unsigned char* getWeatherSymbol(int code) {
    // WMO Weather interpretation codes (WW)
    // Return a simple ASCII/Unicode symbol based on code
    if (code == 0) return weather_icon_clear; // Clear
    if (code <= 3) return weather_icon_cloudy; // Cloudy
    if (code <= 9) return weather_icon_rain; // Rain
    if (code <= 19) return weather_icon_drizzle; // Drizzle
    if (code <= 39) return weather_icon_snow; // Snow
    if (code <= 49) return weather_icon_fog; // Fog
    if (code <= 59) return weather_icon_rain; // Rain
    if (code <= 69) return weather_icon_snow; // Snow
    if (code <= 79) return weather_icon_rain; // Rain
    if (code <= 99) return weather_icon_thunderstorm; // Thunderstorm
    return weather_icon_unknown; // Unknown
}
